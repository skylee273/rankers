package com.project.rankers.retrofit.items

import com.google.gson.annotations.SerializedName

class ContestItem {
    @SerializedName("ID")
    val id : String = ""
    @SerializedName("EMAIL")
    val email : String = ""
    @SerializedName("TITLE")
    val title : String = ""
    @SerializedName("START")
    val start : String = ""
    @SerializedName("END")
    val end : String = ""
    @SerializedName("TYPE")
    val type : String = ""
    @SerializedName("HOST")
    val host : String = ""
    @SerializedName("LOCATION")
    val location : String = ""
    @SerializedName("DEPART")
    val depart : String = ""

}